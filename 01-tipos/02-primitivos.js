let numero = 1;
let texto = "Hola Mundo";
let verdadero = true;
let falso = false;
let noDefinido;
let undef = undefined;
let nulo = null;

console.log(numero); // imprime el valor de la variable 'numero'
console.log("Texto: ", texto); // muestra el literal y el valor de la variable 'texto'
console.log(`Verdadero: ${verdadero}`); // muestra un mensaje con una variable interpolada en él
console.log(`Falso: ${falso}`); // interpolación de variables en template literales (ES6+)
console.log("No definido: ", noDefinido); // muestra que el valor es 'undefined'
console.log("Undefined: ", undef); // muestra que el valor es 'undefined'
console.log("Nulo: ", nulo); // muestra que el valor es 'null'